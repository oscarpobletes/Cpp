#include <iostream>
using namespace std;
  
int main()
{
    /* The While loop is an entry control loop because firstly, 
	the condition is checked, then the loop's body is executed. */
    int i = 1;
  
    // test expression
    while (i > -5) {
        cout << i << "\n";
        i--;
    }
  
    return 0;
}
